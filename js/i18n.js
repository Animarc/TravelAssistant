    /**
     * Get translation for a key
     * @param {string} key - Translation key
     * @returns {string} Translated string
     */
    t(key) {
        return this.translations[this.currentLang]?.[key] || this.translations.es[key] || key;
    },

    /**
     * Set current language
     * @param {string} lang - Language code
     */
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('travelAssistantLang', lang);
            this.updateUI();
        }
    },

    /**
     * Load saved language from localStorage
     */
    loadSavedLanguage() {
        const saved = localStorage.getItem('travelAssistantLang');
        if (saved && this.translations[saved]) {
            this.currentLang = saved;
        }
    },

    /**
     * Update all UI elements with current language
     */
    updateUI() {
        // Update navbar
        document.querySelector('.navbar-title').textContent = this.t('appTitle');
        document.getElementById('planificacionBtn').textContent = this.t('planning');
        document.getElementById('presupuestoBtn').textContent = this.t('budget');
        document.getElementById('objetosBtn').textContent = this.t('objects');

        // Update toolbar
        document.getElementById('addActivityBtn').textContent = this.t('addActivity');
        document.getElementById('addAccommodationBtn').textContent = this.t('addAccommodation');

        // Update day header buttons
        document.getElementById('moveDayLeftBtn').textContent = this.t('moveDayBack');
        document.getElementById('addDayBtn').textContent = this.t('addDayFull');
        document.getElementById('moveDayRightBtn').textContent = this.t('moveDayForward');

        // Update footer
        const footerText = document.getElementById('footerText');
        if (footerText) {
            footerText.innerHTML = `${this.t('footerBeta')} | ${this.t('footerContact')}: <a href="mailto:animarc@gmail.com">animarc@gmail.com</a>`;
        }

        // Update trip name
        const tripNameEl = document.getElementById('tripName');
        if (tripNameEl && typeof State !== 'undefined') {
            tripNameEl.textContent = State.tripName || this.t('appTitle');
        }

        // Refresh current view
        if (typeof Views !== 'undefined') {
            Views.refresh();
        }
    },

